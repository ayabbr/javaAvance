

public class Cell {
	
private	final String str;
	
private	Cell next;
	
	public Cell (String str, Cell next) {
		
		this.str=str;
		this.next=next;
		
	}
	
	public String getStr() {
		return str;
	}

	
	public Cell getNext() {
		return next;
	}
	public void setNext(Cell next) {
		this.next = next;
	}
	
	
	
		
		
	}
	
	


